#ifndef STACK_H_
#define STACK_H_

/*******************************************************************************
 *                       	Included Libraries                                 *
 *******************************************************************************/
#include"std_types.h"
#include <stdio.h>
#include<stdlib.h>

/*******************************************************************************
 *                        	Definitions				                           *
 *******************************************************************************/
#define Push 1
#define Pop 2
#define Exit 3

/*******************************************************************************
 *                         Stack Structures                   		           *
 *******************************************************************************/
typedef struct
{
	uint8 *array;
	uint8 capacity;
	sint16 top;
}stack;

/*******************************************************************************
 *                         Status Enum				                           *
 *******************************************************************************/
typedef enum
{
	EMPTY,
	FULL,
	WORKING
}status;

/*******************************************************************************
 *                         Extern Variables				                       *
 *******************************************************************************/
extern stack * new_stack;

/*******************************************************************************
 *                      Functions Prototypes                                   *
 *******************************************************************************/

/*******************************************************************************
 * Function Name:	CreatStack
 *
 * Description: 	Create tStack
 *
 * Inputs:			uint8
 *
 * Return:			pointer to struct
 *******************************************************************************/
stack * CreatStack(uint8 capacity);

/*******************************************************************************
 * Function Name:	push
 *
 * Description: 	add element to Stack
 *
 * Inputs:			pointer to Stack struct
 * 					uint8
 *
 * Return:			enum Status
 *******************************************************************************/
status push(stack * stack_ptr,uint8 data);

/*******************************************************************************
 * Function Name:	pop
 *
 * Description: 	remove element to Stack
 *
 * Inputs:			pointer to Stack struct
 *
 * Return:			enum Status
 *******************************************************************************/
status pop(stack * stack_ptr);

#endif /* STACK_H_ */
